// while

let index = 0
while(index <= 10)
{
    // console.log(`Value of index is ${index}`)
    index = index + 2
}

let myArray = [1,2,3,4,5]
let arr = 0
while(arr < myArray.length){
    // console.log(`Value is ${myArray[arr]}`)
    arr = arr + 1
}

// ------------ do while ------------------------->

let score = 11

do {
    console.log(`Score is ${score}`) // Score is 11
    score++
} while(score <= 10)
